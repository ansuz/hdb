// dumptable accepts a callback

// if no callback is provided, it defaults to 'console.log'
// your callback can do more than just display

// below, I use array.filter to return only those nodes
// which are using a cjdns version older than '10'


require("../dumptable.js")(function(table){
  table=table.filter(function(node){
    return node.version < 10;
  });
  console.log(table);
});
